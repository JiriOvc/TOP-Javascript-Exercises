// Operations

function add(a, b) {
    return a + b;
};

function subtract(a, b) {
    return a - b;
};

function multiply(a, b) {
    return a * b;
};

function divide(a, b) {
    return a / b;
};

console.log(add(9, 3))
console.log(subtract(9, 3))
console.log(multiply(9, 3))
console.log(divide(9, 3))